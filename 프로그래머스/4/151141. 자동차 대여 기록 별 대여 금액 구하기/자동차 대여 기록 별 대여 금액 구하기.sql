-- 트럭 대여 기간 및 DAILY_FEE 추출
WITH TRUCK_DURATION AS (
SELECT HISTORY_ID, CAR_ID, (DATEDIFF(END_DATE, START_DATE)+1) AS DURATION,
                        CASE WHEN (DATEDIFF(END_DATE, START_DATE)+1)>= 90 THEN '90일 이상'
                                WHEN (DATEDIFF(END_DATE, START_DATE)+1)>= 30 THEN '30일 이상'
                                WHEN (DATEDIFF(END_DATE, START_DATE)+1)>= 7 THEN '7일 이상'
                                END AS DURATION_RATE, DAILY_FEE
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY JOIN CAR_RENTAL_COMPANY_CAR C USING(CAR_ID)
WHERE CAR_TYPE = '트럭'
),

-- 트럭 할인 금액만 가지고 오기
TRUCK_DISCOUNT AS (
SELECT DURATION_TYPE, DISCOUNT_RATE
FROM CAR_RENTAL_COMPANY_DISCOUNT_PLAN 
WHERE CAR_TYPE = '트럭'
)

-- 정답
SELECT HISTORY_ID, ROUND(DURATION*DAILY_FEE*(1-IFNULL(DISCOUNT_RATE,0)*0.01)) AS FEE
FROM TRUCK_DURATION DU LEFT JOIN  TRUCK_DISCOUNT DI ON DI.DURATION_TYPE = DU.DURATION_RATE
ORDER BY 2 DESC, 1 DESC;






















